        for (ArchiveInventory item : generalList) {
            Map<String, Object> archiveEntry = new HashMap<>();
            archiveEntry.put("archiveId", item.getArchiveId());
            archiveEntry.put("sourceTable", "Inventory");
            
            Map<String, Object> dataMap = new HashMap<>();

            if (item.getProductname() != null) {
                dataMap.put("item_name", item.getProductname());
            }
            if (item.getGroupName() != null) {
                dataMap.put("location", item.getGroupName());
            }
            // add other fields in the same way

            archiveEntry.put("data", dataMap);
            
            
            archiveEntry.put("canDeletePermanently", true); // assume true for now
            result.add(archiveEntry);
        }

        // --- Fine Chemical Archive ---
        List<ArchiveFineChemicalInventory> fineList = archiveFineChemicalRepo.findAll();
        for (ArchiveFineChemicalInventory item : fineList) {
            Map<String, Object> archiveEntry = new HashMap<>();
            archiveEntry.put("archiveId", item.getArchiveId());
            archiveEntry.put("sourceTable", "finechemicalinventory");
            
            Map<String, Object> dataMap = new HashMap<>();

            if (item.getProductname() != null) {
                dataMap.put("product_name", item.getProductname());
            }
            if (item.getCompanyname() != null) {
                dataMap.put("company", item.getCompanyname());
            }
            if (item.getCasnumber() != null) {
                dataMap.put("cas_number", item.getCasnumber());
            }
            if (item.getHazardousSubstance() != null) {
                dataMap.put("hazardous_substance", item.getHazardousSubstance());
            }

            archiveEntry.put("data", dataMap);

            
            
            Map<String, Object> deletedByMap = new HashMap<>();

            if (user.getName() != null) {
                deletedByMap.put("userId", user.getName());
            }
            if (user.getRole() != null) {
                deletedByMap.put("role", user.getRole());
            }

            archiveEntry.put("deletedBy", deletedByMap);

            archiveEntry.put("canDeletePermanently", true);
            result.add(archiveEntry);
        }

