    private static <T> void setIfNotNull(java.util.function.Consumer<T> setter, T value) {
        if (value != null) setter.accept(value);
    }

    private static void setIfNotBlank(java.util.function.Consumer<String> setter, String value) {
        if (value != null && !value.trim().isEmpty()) setter.accept(value.trim());
    }

    private static String firstNonBlank(String a, String b) {
        if (a != null && !a.trim().isEmpty()) return a.trim();
        if (b != null && !b.trim().isEmpty()) return b.trim();
        return null;
    }

    private static Integer parseInteger(String s) {
        if (s == null) return null;
        try { return Integer.valueOf(s.trim()); }
        catch (NumberFormatException e) { return null; }
    }

    private static String boolToString(Boolean b) {
        return b == null ? null : String.valueOf(b); // "true"/"false"
        // Alternatively: return b == null ? null : (b ? "YES" : "NO");
    }

    private static String dateToString(Date d) {
        return d == null ? null : ISO_DATE_TIME.format(d);
    }
    private static final SimpleDateFormat ISO_DATE_TIME = new SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss.SSSZ");
    /**
     * Parse a JSON array string like ["GHS01","GHS02"] into List<String>.
     * If Jackson is available, prefer that. Fallback is a very simple parser.
     */
    private static List<String> parseJsonStringArray(String json) {
        if (json == null || json.trim().isEmpty()) return null;
        // If Jackson available:
        // try { return MAPPER.readValue(json, new TypeReference<List<String>>(){}); }
        // catch (Exception e) { return null; }

        // Minimal fallback: handles ["a","b"] or comma-separated without quotes.
        String s = json.trim();
        if (s.startsWith("[") && s.endsWith("]")) {
            s = s.substring(1, s.length() - 1).trim();
        }
        if (s.isEmpty()) return Collections.emptyList();
        String[] parts = s.split(",");
        List<String> out = new ArrayList<>(parts.length);
        for (String p : parts) {
            String v = p.trim();
            if (v.startsWith("\"") && v.endsWith("\"") && v.length() >= 2) {
                v = v.substring(1, v.length() - 1);
            }
            if (!v.isEmpty()) out.add(v);
        }
        return out;
    }

    /**
     * Flatten a JSON array string into a comma-separated string for VO.glsCheckbox
     * If already a simple string, returns it as-is.
     */
    private static String flattenJsonStringArray(String json) {
        List<String> list = parseJsonStringArray(json);
        if (list == null) return null;
        return String.join(",", list);
    }
    
