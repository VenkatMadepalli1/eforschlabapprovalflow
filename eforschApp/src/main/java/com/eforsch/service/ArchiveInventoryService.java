    public static ArchiveInventoryVO toVO(ArchiveFineChemicalInventory src) {
        if (src == null) return null;

        ArchiveInventoryVO vo = new ArchiveInventoryVO();

        // IDs
        vo.setArchiveId(src.getArchiveId());
        vo.setProductId(src.getProductId());

        // Renamed fields
        setIfNotBlank(vo::setProductName, src.getProductname());
        setIfNotBlank(vo::setCompanyName, src.getCompanyname());

        // Catalogue/catalogue (keep VO.catalogue as provided)
        String cat = firstNonBlank(src.getCatalogue(), src.getCatalogue());
        setIfNotBlank(vo::setCatalogue, cat);

        // Quantity: String -> Integer
        setIfNotNull(vo::setQuantity, parseInteger(src.getQuantity()));

        // Budget (VO has both budgetno & budgetNo)
        setIfNotBlank(vo::setBudgetno, src.getBudgetno());
        setIfNotBlank(vo::setBudgetNo, src.getBudgetno());

        // Price: prefer amount(BigDecimal) else price(double)
        Double price = src.getAmount() != null ? src.getAmount().doubleValue()
                                               : (src.getPrice() != 0.0 ? src.getPrice() : null);
        setIfNotNull(vo::setPrice, price);

        // Dates
        setIfNotNull(vo::setExpiryDate, src.getExpiryDate());
        setIfNotNull(vo::setOrderdate,  src.getOrderdate());

        // People / misc
        setIfNotBlank(vo::setOrderedby, src.getOrderedby());
        setIfNotBlank(vo::setConcentration, src.getConcentration());
        setIfNotBlank(vo::setRemarks, src.getRemarks());
        setIfNotBlank(vo::setCasNumber, src.getCasnumber());

        // Company/material/qty fields (name differences)
        setIfNotBlank(vo::setCompanyinternalno, src.getCompanyInternalNo());
        setIfNotBlank(vo::setSapmaterialno,     src.getSapMaterialNo());
        setIfNotBlank(vo::setWeightvolsubqty,   src.getWvsubqty());

        // Storage & grouping
        setIfNotBlank(vo::setStorageLocation, src.getStorageLocation());
        setIfNotBlank(vo::setGroupName, src.getGroupName());

        // Hazard flags: VO uses String; entity has Boolean
        setIfNotBlank(vo::setHazardousSubstance, boolToString(src.getHazardousSubstance()));
        setIfNotBlank(vo::setCmrSubstance,       boolToString(src.getCmrSubstance()));
        setIfNotBlank(vo::setSkinResorptive,     boolToString(src.getSkinResorptive()));

        // GHS fields (entity stores JSON strings)
        setIfNotNull(vo::setGhsSymbols, parseJsonStringArray(src.getGhsSymbols()));   // -> List<String>
        setIfNotBlank(vo::setGhsCheckbox, flattenJsonStringArray(src.getGhsSignalWord())); // comma-joined

        // H/P phrases & substitution
        setIfNotBlank(vo::sethPhrases, src.gethPhrases());
        setIfNotBlank(vo::setpPhrases, src.getpPhrases());
        setIfNotBlank(vo::setSubstitutionCheck, firstNonBlank(src.getSubstitutionCheck(), src.getSubstitutionOption()));

        // Attachment
        if (src.getFileContent() != null && src.getFileContent().length > 0) {
            vo.setAttachment(src.getFileContent());
        }

        // createdAt in VO is String; entity has Date
        setIfNotBlank(vo::setCreatedAt, dateToString(src.getCreatedAt()));

        // Fields not present in entity -> leave as defaults/null (role, approvals, inventoryType, status, etc.)
        // Optionally surface visibility flag:
        // vo.setStatus(src.isVisibleToUsers() ? "VISIBLE" : "HIDDEN");

        return vo;
    }
    
    
