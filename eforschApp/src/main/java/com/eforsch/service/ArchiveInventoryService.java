    public Map<String, Object> softDeleteFromArchive(DeleteInventoryRequest request) {
        String type = request.getInventoryType().toLowerCase();
        String role = request.getUser().getRole();
        long userId = request.getUser().getId();

		/*
		 * if (!"LABMGMT".equalsIgnoreCase(role)) { throw new
		 * RuntimeException("Unauthorized. Only Lab Management can perform this action."
		 * ); }
		 */

        if ("generalInventory".equalsIgnoreCase(type)) {
        	Optional<ArchiveInventory> archiveOpt = archiveInventoryRepo.findByProductId(request.getProduID());
            if (archiveOpt.isPresent()) {
                ArchiveInventory archive = archiveOpt.get();
                archive.setVisibleToUsers(false);
                archiveInventoryRepo.save(archive);

                return Map.of(
                    "message", "Archive item soft-deleted (not visible to regular users anymore)",
                    "archiveId", archive.getArchiveId(),
                    "deletedBy", Map.of("userId", userId, "role", role)
                );
            }
        } else {
        	Optional<ArchiveFineChemicalInventory> archiveOpt = archiveFineChemicalRepo.findByProductId(request.getProduID().intValue());
            if (archiveOpt.isPresent()) {
                ArchiveFineChemicalInventory archive = archiveOpt.get();
                archive.setVisibleToUsers(false);
                archiveFineChemicalRepo.save(archive);

                return Map.of(
                    "message", "Archive item soft-deleted (not visible to regular users anymore)",
                    "archiveId", archive.getArchiveId(),
                    "deletedBy", Map.of("userId", userId, "role", role)
                );
            }
        }

        throw new RuntimeException("Archive item not found or invalid type");
    }
    
    public Map<String, Object> permanentDeleteFromArchiveByProductId(DeleteInventoryRequest request) {
        String type = request.getInventoryType().toLowerCase();
        String role = request.getUser().getRole();
        long userId = request.getUser().getId();
        String userRole = role.toLowerCase();
        Date deletionTime = new Date();

		/*
		 * if (!"LABMGMT".equalsIgnoreCase(role)) { throw new
		 * RuntimeException("Unauthorized. Only Lab Management can perform this action."
		 * ); }
		 */

        if ("generalInventory".equalsIgnoreCase(type)) {
            // Search archive by productId
            Optional<ArchiveInventory> archiveOpt = archiveInventoryRepo.findByProductId(request.getProduID());
            if (archiveOpt.isPresent()) {
                ArchiveInventory archive = archiveOpt.get();
                archiveInventoryRepo.deleteById(archive.getArchiveId());
                return Map.of(
                    "message", "Archive item permanently deleted",
                    "archiveId", archive.getArchiveId(),
                    "deletedAt", deletionTime,
                    "deletedBy", Map.of("userId", userId, "role", userRole)
                );
            }
        } else {
            Optional<ArchiveFineChemicalInventory> archiveOpt = archiveFineChemicalRepo.findByProductId(request.getProduID().intValue());
            if (archiveOpt.isPresent()) {
                ArchiveFineChemicalInventory archive = archiveOpt.get();
                archiveFineChemicalRepo.deleteByArchiveId(archive.getArchiveId());
                return Map.of(
                    "message", "Archive item permanently deleted",
                    "archiveId", archive.getArchiveId(),
                    "deletedAt", deletionTime,
                    "deletedBy", Map.of("userId", userId, "role", userRole)
                );
            }
        }

        throw new RuntimeException("Archive item not found for provided product ID and inventory type");
    }
    
